<script setup lang="ts">
useHead({
  title: 'Mantenimiento - GRORSHOP',
  meta: [
    {
      name: 'description',
      content: 'GRORSHOP está temporalmente en mantenimiento'
    }
  ]
})

// Verificar cada 5 segundos si el mantenimiento se desactivó
let checkInterval: NodeJS.Timeout | null = null

const checkMaintenanceStatus = async () => {
  try {
    const { maintenanceMode } = await $fetch<{ maintenanceMode: boolean }>('/api/settings/maintenance')

    // Si el mantenimiento se desactivó, redirigir al inicio
    if (!maintenanceMode) {
      if (checkInterval) {
        clearInterval(checkInterval)
      }
      await navigateTo('/')
    }
  } catch (error) {
    console.error('Error al verificar estado de mantenimiento:', error)
  }
}

onMounted(() => {
  // Verificar inmediatamente
  checkMaintenanceStatus()

  // Verificar cada 5 segundos
  checkInterval = setInterval(checkMaintenanceStatus, 5000)
})

onUnmounted(() => {
  if (checkInterval) {
    clearInterval(checkInterval)
  }
})
</script>
